#isupper() checks whether all the case-based characters (letters) of the string are uppercase.

str1 = 'WELCOME'

print(str1.isupper())

print(str1.islower())

#islower() checks whether all the case-based characters (letters) of the string are lowercase.

str2 = 'you all'

print(str2.islower())

print(str2.isupper())